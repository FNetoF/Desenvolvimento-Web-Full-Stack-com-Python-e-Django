let numero = 280.99;

console.log(Math.PI); //  Math.PI > Retorna o valor de PI.

console.log(Math.ceil(numero)); // Math.ceil(numero) > Arredonda pra cima.

console.log(Math.floor(numero)); // Math.floor(numero) > Arredonda para baixo.

console.log(Math.round(numero)); // Math.round(numero) > Arredonda de maneira normal.

console.log(Math.random()); // Math.random() > Gera um número entre 0 e 1 de forma aleatória.

console.log(Math.random() * 100); // Gera um número entre 0 e 100 de forma aleatória.

console.log(Math.min(15, 100)); // Math.min(num, num) > Descobrir o ninimo entre dois números

console.log(Math.max(15, 100)); // Math.max(num, num) > Descobrir o máximo entre dois números

console.log(Math.pow(2, 10)); // Math.pow(variável, expoente).

console.log(Math.sqrt(9)); // Math.sqrt(num) > Decobrir a raiz quadrada de um número

